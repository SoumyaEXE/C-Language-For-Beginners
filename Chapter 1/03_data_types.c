#include <stdio.h>
int main() {

    // Int Stores Only Numbers Like : 1, 2 ,3 ,-99, 67
    // Int Takes Memory Of "4 Bytes" (32 Bits)
    int a;
    a = 1;
    printf("The Value Of Integer Is: %d\n", a);


    // float Stores Only Decimels Like : 1.2, 23.1, 98.2
    // float Takes Memory Of "1 Bytes" (8 Bits)
    float b;
    b = 3.2;
    printf("The Value Of Float Is: %f\n", b);


    // char Stores Characters Like : S, a , b. (Do Not Support More Than One Character Together Like Soumya Etc.)
    // char Takes Memory Of "4 Bytes" (32 Bits)
    char c;
    c = 'R';
    printf("The Value Of Char Is: %c\n", c);

}